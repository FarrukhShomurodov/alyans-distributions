    public function run(): void
    {
        $categories = Category::where('is_active', true)->get()->keyBy('name');

        if ($categories->isEmpty()) {
            $this->command->warn('Нет категорий — продукты не будут созданы.');
            return;
        }

        $catalog = $this->catalog();

        $created = 0;
        $sku = 10000;

        foreach ($catalog as $categoryName => $products) {
            $category = $categories->get($categoryName);
            if (!$category) {
                $this->command->warn("Категория «{$categoryName}» не найдена — пропускаем.");
                continue;
            }

            foreach ($products as $item) {
                $this->createProduct($category->id, $item, $sku++);
                $created++;
            }
        }

        $this->command->info("Создано {$created} товаров.");
    }

    private function createProduct(int $categoryId, array $item, int $sku): void
    {
        [$name, $price, $discount] = $item + [null, 0, 0];

        $product = Product::create([
            'name' => $name,
            'category_id' => $categoryId,
            'price' => $price,
            'discount_percent' => $discount,
            'external_id' => 'SM-' . str_pad((string) $sku, 6, '0', STR_PAD_LEFT),
            'is_active' => true,
        ]);

        $product->stock()->create([
            'quantity' => random_int(15, 250),
        ]);
    }

